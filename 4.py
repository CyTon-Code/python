# Суму S квадратів усіх елементів, що перевищують 10 по абсолютному
# значенню і їхню кількість К.

# Кожен елемент від -10 до 10 піносиш до квадрату і знаходиш їхню суму
# і кількість цифр від -10 до 10
n = 10
sum = 0

for i in range(n * 2):
    sum += i ** 2

print(sum)

# або:
sum = 0

array = [
[1,2,3],
[3,1,2],
[2,3,1]
]
for j in array:
    for i in j:
        if i > 10:
            sum += i

print(sum)
